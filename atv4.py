#Encontrar o valor máximo e mínimo em uma lista de números:
#Use `max()` e `min()` para encontrar os valores máximo e mínimo.

lista = [4, 2, 8, 5, 10, 12, 3]
print('Lista de números: ', lista)

print('O maior número da lista é: ', max(lista))

print('O menor número da lista é:', min(lista))
