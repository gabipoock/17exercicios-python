#Calcular a soma de uma lista de listas:
#Use `(+)` para calcular a soma dos elementos em várias listas aninhadas.

lista1 = [1, 2, 3]
print('Primeira lista de números: ', lista1)

lista2 = [4, 5, 6]
print('Segunda lista de números: ', lista2)

lista3 = [7, 8, 9]
print('Terceira lista de números: ', lista3)

print('A soma dos números da lista é: ', sum(lista1) + sum(lista2) + sum(lista3))



